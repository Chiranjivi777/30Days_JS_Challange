//Use object.keys and object.values methods to log all the keys and values of the book object.


// Create an object representing a book
let book = {
    title: "To Kill a Mockingbird",
    author: "Harper Lee",
    year: 1960
};

// Log all the keys of the book object
console.log("Keys of the book object:");
console.log(Object.keys(book));

// Log all the values of the book object
console.log("Values of the book object:");
console.log(Object.values(book));
