//Create a script that compares two numbers using different comparison operators and combines conditions using logical operators, logging the results.

let number1 = 10;
let number2 = 5;

console.log(number1 > number2 && `${number1} is greater than ${number2}` || `${number1} is not greater than ${number2}`);
console.log(number1 < number2 && `${number1} is less than ${number2}` || `${number1} is not less than ${number2}`);
console.log(number1 === number2 && `${number1} is equal to ${number2}` || `${number1} is not equal to ${number2}`);
console.log(number1 !== number2 && `${number1} is not equal to ${number2}` || `${number1} is equal to ${number2}`);

