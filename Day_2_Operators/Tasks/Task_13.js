//Task 13: Write a program that uses the ! operator to negate a condition and log the result to the console.

let isSunny = false;

if (!isSunny) {
    console.log("It is not sunny today");
} else {
    console.log("It is sunny today");
}


/* In JavaScript, the `!` operator is known as the logical NOT operator. It is used to negate a boolean value, meaning it reverses the logical state of a condition. When you use `!` in front of a boolean expression, it will return the opposite boolean value. This is particularly useful when you want to check for the opposite of a condition or when you need to invert the result of a comparison.*/