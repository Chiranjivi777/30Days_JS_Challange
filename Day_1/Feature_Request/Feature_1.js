//Variable Types Console Log: Write a script that declares variables of different data types and logs both the value and type of each variable to the console.


// Declare variables of different data types
let numberVar = 10;
let stringVar = "Hello, World!";
let booleanVar = true;
let arrayVar = [1, 2, 3];
let objectVar = { key: "value" };
let nullVar = null;
let undefinedVar;

// Log the value and type of each variable
console.log("Number Variable:", numberVar, typeof numberVar);
console.log("String Variable:", stringVar, typeof stringVar);
console.log("Boolean Variable:", booleanVar, typeof booleanVar);
console.log("Array Variable:", arrayVar, typeof arrayVar);
console.log("Object Variable:", objectVar, typeof objectVar);
console.log("Null Variable:", nullVar, typeof nullVar);
console.log("Undefined Variable:", undefinedVar, typeof undefinedVar);