let number = 10; // You can change this number to test with different values

if (number > 0) {
    console.log(`The number ${number} is positive.`);
} else if (number < 0) {
    console.log(`The number ${number} is negative.`);
} else {
    console.log(`The number ${number} is zero.`);
}
